/* =========================================================
   SCRIPT DE CONSTRAINTS (FOREIGN KEYS)
   ========================================================= */

-- 1. Empleados
ALTER TABLE CC_EMPLEADO ADD CONSTRAINT FK_EMPLEADO_AREA FOREIGN KEY (ID_AREA) REFERENCES CC_AREA(ID_AREA);
ALTER TABLE CC_EMPLEADO ADD CONSTRAINT FK_EMPLEADO_ROL FOREIGN KEY (ID_ROL) REFERENCES CC_ROL(ID_ROL);
ALTER TABLE CC_EMPLEADO ADD CONSTRAINT FK_EMPLEADO_JEFE FOREIGN KEY (ID_JEFE_DIRECTO) REFERENCES CC_EMPLEADO(ID_EMPLEADO);

-- 2. Cuentas
ALTER TABLE CC_CUENTA_BANCARIA ADD CONSTRAINT FK_CUENTA_EMPLEADO FOREIGN KEY (ID_EMPLEADO) REFERENCES CC_EMPLEADO(ID_EMPLEADO);
ALTER TABLE CC_CUENTA_BANCARIA ADD CONSTRAINT FK_CUENTA_MONEDA FOREIGN KEY (ID_MONEDA) REFERENCES CC_MONEDA(ID_MONEDA);

-- 3. Fondos (Padre e Hijos)
ALTER TABLE CC_FONDO ADD CONSTRAINT FK_FONDO_MONEDA FOREIGN KEY (ID_MONEDA) REFERENCES CC_MONEDA(ID_MONEDA);
ALTER TABLE CC_FONDO ADD CONSTRAINT FK_FONDO_CUENTA FOREIGN KEY (ID_CUENTA_BANCARIA) REFERENCES CC_CUENTA_BANCARIA(ID_CUENTA_BANCARIA);
ALTER TABLE CC_FONDO ADD CONSTRAINT FK_FONDO_RESPONSABLE FOREIGN KEY (ID_RESPONSABLE) REFERENCES CC_EMPLEADO(ID_EMPLEADO);

ALTER TABLE CC_CAJA_CHICA ADD CONSTRAINT FK_CAJA_FONDO_PADRE FOREIGN KEY (ID_FONDO) REFERENCES CC_FONDO(ID_FONDO);
ALTER TABLE CC_CAJA_CHICA ADD CONSTRAINT FK_CAJA_AREA FOREIGN KEY (ID_AREA) REFERENCES CC_AREA(ID_AREA);

ALTER TABLE CC_ENTREGA_RENDIR ADD CONSTRAINT FK_ENTREGA_FONDO_PADRE FOREIGN KEY (ID_FONDO) REFERENCES CC_FONDO(ID_FONDO);
ALTER TABLE CC_ENTREGA_RENDIR ADD CONSTRAINT FK_ENTREGA_SOLICITANTE FOREIGN KEY (ID_SOLICITANTE) REFERENCES CC_EMPLEADO(ID_EMPLEADO);
ALTER TABLE CC_ENTREGA_RENDIR ADD CONSTRAINT FK_ENTREGA_APROBADOR FOREIGN KEY (ID_APROBADOR) REFERENCES CC_EMPLEADO(ID_EMPLEADO);

-- Relación: Entrega -> Rendición
ALTER TABLE CC_ENTREGA_RENDIR ADD CONSTRAINT FK_ENTREGA_RENDICION FOREIGN KEY (ID_RENDICION) REFERENCES CC_RENDICION(ID_RENDICION);

-- 4. Ciclo de Caja
ALTER TABLE CC_CICLO_CAJA ADD CONSTRAINT FK_CICLO_CAJA_PADRE FOREIGN KEY (ID_CAJA_CHICA) REFERENCES CC_CAJA_CHICA(ID_FONDO);
-- Relación Ciclo -> Rendición
ALTER TABLE CC_CICLO_CAJA ADD CONSTRAINT FK_CICLO_RENDICION FOREIGN KEY (ID_RENDICION) REFERENCES CC_RENDICION(ID_RENDICION);

-- 5. Solicitud de Gasto (Caja Chica)
ALTER TABLE CC_SOLICITUD_GASTO ADD CONSTRAINT FK_SOLICITUD_EMPLEADO FOREIGN KEY (ID_SOLICITANTE) REFERENCES CC_EMPLEADO(ID_EMPLEADO);
ALTER TABLE CC_SOLICITUD_GASTO ADD CONSTRAINT FK_SOLICITUD_CICLO FOREIGN KEY (ID_CICLO_CAJA) REFERENCES CC_CICLO_CAJA(ID_CICLO_CAJA);

-- 6. Comprobantes (Documentos de Gasto)
ALTER TABLE CC_COMPROBANTE_PAGO ADD CONSTRAINT FK_COMPROBANTE_RUBRO FOREIGN KEY (ID_RUBRO) REFERENCES CC_RUBRO(ID_RUBRO);
ALTER TABLE CC_COMPROBANTE_PAGO ADD CONSTRAINT FK_COMPROBANTE_MONEDA FOREIGN KEY (ID_MONEDA) REFERENCES CC_MONEDA(ID_MONEDA);

ALTER TABLE CC_COMPROBANTE_PAGO ADD CONSTRAINT FK_COMPROBANTE_SOLICITUD FOREIGN KEY (ID_SOLICITUD_GASTO) REFERENCES CC_SOLICITUD_GASTO(ID_SOLICITUD_GASTO);
ALTER TABLE CC_COMPROBANTE_PAGO ADD CONSTRAINT FK_COMPROBANTE_ENTREGA FOREIGN KEY (ID_ENTREGA_RENDIR) REFERENCES CC_ENTREGA_RENDIR(ID_FONDO);

-- 7. Transacciones
ALTER TABLE CC_TRANSACCION ADD CONSTRAINT FK_TRANSACCION_CTA_ORIGEN FOREIGN KEY (ID_CUENTA_ORIGEN) REFERENCES CC_CUENTA_BANCARIA(ID_CUENTA_BANCARIA);
ALTER TABLE CC_TRANSACCION ADD CONSTRAINT FK_TRANSACCION_CTA_DESTINO FOREIGN KEY (ID_CUENTA_DESTINO) REFERENCES CC_CUENTA_BANCARIA(ID_CUENTA_BANCARIA);
ALTER TABLE CC_TRANSACCION ADD CONSTRAINT FK_TRANSACCION_MONEDA FOREIGN KEY (ID_MONEDA) REFERENCES CC_MONEDA(ID_MONEDA);

--Historial de empleados
ALTER TABLE CC_HISTORIAL_JEFATURA ADD CONSTRAINT PK_HIST_JEFATURA PRIMARY KEY (ID_HISTORIAL);
ALTER TABLE CC_HISTORIAL_JEFATURA ADD CONSTRAINT FK_HIST_EMP FOREIGN KEY (ID_EMPLEADO) REFERENCES CC_EMPLEADO(ID_EMPLEADO);
ALTER TABLE CC_HISTORIAL_JEFATURA ADD CONSTRAINT FK_HIST_JEFE FOREIGN KEY (ID_JEFE) REFERENCES CC_EMPLEADO(ID_EMPLEADO);

