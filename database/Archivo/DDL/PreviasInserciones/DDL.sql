);

-- =============================================
-- 2. ACTORES Y CUENTAS
-- =============================================

CREATE TABLE CC_EMPLEADO (
    ID_EMPLEADO NUMBER PRIMARY KEY,
    NOMBRES_EMPLEADO VARCHAR2(60),
    APELLIDO_PATERNO_EMPLEADO VARCHAR2(40),
    APELLIDO_MATERNO_EMPLEADO VARCHAR2(40),
    CORREO_INSTITUCIONAL_EMPLEADO VARCHAR2(100),
    NUMERO_CELULAR_EMPLEADO VARCHAR2(15),
    ID_AREA NUMBER,          
    ID_ROL NUMBER,           
    ID_JEFE_DIRECTO NUMBER,  
    -- Auditoría
    USUARIO_CREACION VARCHAR2(50),
    FECHA_CREACION DATE,
    USUARIO_MODIFICACION VARCHAR2(50),
    FECHA_MODIFICACION DATE
);

CREATE TABLE CC_CUENTA_BANCARIA (
    ID_CUENTA_BANCARIA NUMBER PRIMARY KEY,
    BANCO_CUENTA_BANCARIA VARCHAR2(50),
    NUMERO_CUENTA_BANCARIA VARCHAR2(30),
    CCI_CUENTA_BANCARIA CHAR(20),
    
    ID_MONEDA NUMBER, 
    
    ES_CUENTA_BANCARIA_PRINCIPAL NUMBER(1) DEFAULT 0, 
    ID_EMPLEADO NUMBER,      
    -- Auditoría
    USUARIO_CREACION VARCHAR2(50),
    FECHA_CREACION DATE,
    USUARIO_MODIFICACION VARCHAR2(50),
    FECHA_MODIFICACION DATE
);
CREATE TABLE CC_HISTORIAL_JEFATURA (
    ID_HISTORIAL NUMBER,
    ID_EMPLEADO NUMBER NOT NULL,
    ID_JEFE NUMBER,         -- Puede ser NULL si es Gerente General
    FECHA_INICIO DATE DEFAULT SYSDATE,
    FECHA_FIN DATE,         -- NULL indica que es el jefe actual
    FECHA_REGISTRO DATE DEFAULT SYSDATE
);

-- =============================================
-- 3. RENDICIÓN (DESTINO FINAL)
