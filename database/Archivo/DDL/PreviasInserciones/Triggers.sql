/* =========================================================
   SCRIPT DE TRIGGERS FINAL - AUDITORÍA Y VALORES POR DEFECTO
   - Lógica optimizada: Modificación se guarda SIEMPRE (Insert o Update).
   - Creación solo en INSERT.
   - Valores por defecto (0 en vez de NULL) solo en INSERT.
   - Evento DELETE removido (no requerido).
   ========================================================= */

-- 1. TABLA: CC_MONEDA
CREATE OR REPLACE TRIGGER TRG_CC_MONEDA
BEFORE INSERT OR UPDATE ON CC_MONEDA
FOR EACH ROW
BEGIN
    IF INSERTING THEN
        :NEW.USUARIO_CREACION := USER;
        :NEW.FECHA_CREACION   := SYSDATE;
    END IF;

    -- Se ejecuta siempre (en Insert o Update) para guardar el último rastro
    :NEW.USUARIO_MODIFICACION := USER;
    :NEW.FECHA_MODIFICACION   := SYSDATE;
END;
/

