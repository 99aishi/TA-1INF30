--15.
CREATE OR REPLACE TRIGGER TRG_CONTROL_HISTORIAL_JEFES
AFTER INSERT OR UPDATE OF ID_JEFE_DIRECTO ON CC_EMPLEADO
FOR EACH ROW
BEGIN
    -- PARTE A: Cerrar el ciclo del jefe anterior (Update fecha fin)
    -- Solo si estamos actualizando y el empleado tenía un jefe previo
    IF UPDATING AND :OLD.ID_JEFE_DIRECTO IS NOT NULL THEN
        UPDATE CC_HISTORIAL_JEFATURA
        SET FECHA_FIN = SYSDATE
        WHERE ID_EMPLEADO = :OLD.ID_EMPLEADO 
          AND FECHA_FIN IS NULL; -- Importante: Cerrar solo el registro vigente
    END IF;

    -- PARTE B: Abrir el nuevo ciclo con el nuevo jefe
    -- Se inserta siempre que hay un cambio o un nuevo empleado
    -- Nota: Si ID_JEFE_DIRECTO es NULL (Gerente), se guarda el histórico indicando NULL (sin jefe)
    INSERT INTO CC_HISTORIAL_JEFATURA (
        ID_HISTORIAL, ID_EMPLEADO, ID_JEFE, FECHA_INICIO, FECHA_FIN
    ) VALUES (
        (SELECT NVL(MAX(ID_HISTORIAL), 0) + 1 FROM CC_HISTORIAL_JEFATURA), -- Generador ID
        :NEW.ID_EMPLEADO,
        :NEW.ID_JEFE_DIRECTO,
        SYSDATE, -- Fecha Inicio es AHORA
        NULL     -- Fecha Fin queda abierta
    );
END;
